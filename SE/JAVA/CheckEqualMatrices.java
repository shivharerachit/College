import java.util.Scanner;

// Java Program to determine whether two matrices are equal

public class CheckEqualMatrices {
    public static void main(String[] args) {
        int[][] matrix1 = inputMatrix();
        int[][] matrix2 = inputMatrix();
        boolean areEqual = areMatricesEqual(matrix1, matrix2);
        System.out.println("Are the matrices equal? " + areEqual);
    }

    public static int[][] inputMatrix() {
        Scanner scanner = new Scanner(System.in);
        System.out.print("Enter the number of rows: ");
        int rows = scanner.nextInt();
        System.out.print("Enter the number of columns: ");
        int columns = scanner.nextInt();
        int[][] matrix = new int[rows][columns];
        System.out.println("Enter the elements of the matrix:");
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < columns; j++) {
                matrix[i][j] = scanner.nextInt();
            }
        }
        return matrix;
    }

    public static boolean areMatricesEqual(int[][] matrix1, int[][] matrix2) {
        if (matrix1.length != matrix2.length || matrix1[0].length != matrix2[0].length) {
            return false;
        }

        for (int i = 0; i < matrix1.length; i++) {
            for (int j = 0; j < matrix1[0].length; j++) {
                if (matrix1[i][j] != matrix2[i][j]) {
                    return false;
                }
            }
        }

        return true;
    }
}